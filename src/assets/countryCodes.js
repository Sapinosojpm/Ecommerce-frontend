// Helper function to format phone number based on country code
export const formatPhoneNumber = (value, countryCode) => {
  const country = countryCodes.find(c => c.code === countryCode);
  if (!country) return value;

  // Remove all non-digit characters
  const phoneNumber = value.replace(/\D/g, '');
  
  // Apply formatting based on country's format
  switch (country.code) {
    case "+63": // Philippines
      if (phoneNumber.length <= 3) return phoneNumber;
      if (phoneNumber.length <= 6) return `${phoneNumber.slice(0, 3)} ${phoneNumber.slice(3)}`;
      return `${phoneNumber.slice(0, 3)} ${phoneNumber.slice(3, 6)} ${phoneNumber.slice(6, 10)}`;
    
    case "+44": // UK
      if (phoneNumber.length <= 4) return phoneNumber;
      return `${phoneNumber.slice(0, 4)} ${phoneNumber.slice(4, 10)}`;
    
    case "+81": // Japan
    case "+82": // South Korea
      if (phoneNumber.length <= 2) return phoneNumber;
      if (phoneNumber.length <= 6) return `${phoneNumber.slice(0, 2)} ${phoneNumber.slice(2)}`;
      return `${phoneNumber.slice(0, 2)} ${phoneNumber.slice(2, 6)} ${phoneNumber.slice(6, 10)}`;
    
    case "+91": // India
      if (phoneNumber.length <= 5) return phoneNumber;
      return `${phoneNumber.slice(0, 5)} ${phoneNumber.slice(5, 10)}`;
    
    case "+60": // Malaysia
      if (phoneNumber.length <= 2) return phoneNumber;
      if (phoneNumber.length <= 6) return `${phoneNumber.slice(0, 2)} ${phoneNumber.slice(2)}`;
      return `${phoneNumber.slice(0, 2)} ${phoneNumber.slice(2, 6)} ${phoneNumber.slice(6, 10)}`;
    
    case "+86": // China
      if (phoneNumber.length <= 3) return phoneNumber;
      if (phoneNumber.length <= 7) return `${phoneNumber.slice(0, 3)} ${phoneNumber.slice(3)}`;
      return `${phoneNumber.slice(0, 3)} ${phoneNumber.slice(3, 7)} ${phoneNumber.slice(7, 11)}`;
    
    case "+55": // Brazil
      if (phoneNumber.length <= 2) return phoneNumber;
      if (phoneNumber.length <= 7) return `${phoneNumber.slice(0, 2)} ${phoneNumber.slice(2)}`;
      return `${phoneNumber.slice(0, 2)} ${phoneNumber.slice(2, 7)} ${phoneNumber.slice(7, 11)}`;
    
    case "+33": // France
      if (phoneNumber.length <= 1) return phoneNumber;
      if (phoneNumber.length <= 3) return `${phoneNumber.slice(0, 1)} ${phoneNumber.slice(1)}`;
      if (phoneNumber.length <= 5) return `${phoneNumber.slice(0, 1)} ${phoneNumber.slice(1, 3)} ${phoneNumber.slice(3)}`;
      if (phoneNumber.length <= 7) return `${phoneNumber.slice(0, 1)} ${phoneNumber.slice(1, 3)} ${phoneNumber.slice(3, 5)} ${phoneNumber.slice(5)}`;
      return `${phoneNumber.slice(0, 1)} ${phoneNumber.slice(1, 3)} ${phoneNumber.slice(3, 5)} ${phoneNumber.slice(5, 7)} ${phoneNumber.slice(7, 9)}`;
    
    default:
      // Default formatting for other countries
      if (phoneNumber.length <= 3) return phoneNumber;
      if (phoneNumber.length <= 6) return `${phoneNumber.slice(0, 3)} ${phoneNumber.slice(3)}`;
      return `${phoneNumber.slice(0, 3)} ${phoneNumber.slice(3, 6)} ${phoneNumber.slice(6, 10)}`;
  }
};

// Helper function to get placeholder based on country code
export const getPlaceholder = (countryCode) => {
  const country = countryCodes.find(c => c.code === countryCode);
  return country ? country.placeholder : "Phone Number";
};

// Helper function to get pattern based on country code
export const getPattern = (countryCode) => {
  const country = countryCodes.find(c => c.code === countryCode);
  if (!country) return "[0-9]{3} [0-9]{3} [0-9]{4}";

  switch (country.code) {
    case "+44": return "[0-9]{4} [0-9]{6}";
    case "+81":
    case "+82": return "[0-9]{2} [0-9]{4} [0-9]{4}";
    case "+91": return "[0-9]{5} [0-9]{5}";
    case "+60": return "[0-9]{2} [0-9]{4} [0-9]{4}";
    case "+86": return "[0-9]{3} [0-9]{4} [0-9]{4}";
    case "+55": return "[0-9]{2} [0-9]{5} [0-9]{4}";
    case "+33": return "[0-9] [0-9]{2} [0-9]{2} [0-9]{2} [0-9]{2}";
    default: return "[0-9]{3} [0-9]{3} [0-9]{4}";
  }
};

